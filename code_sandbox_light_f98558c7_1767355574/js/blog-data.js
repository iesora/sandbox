// ブログ記事をIDで取得する関数
function getBlogArticleById(id) {
    return blogArticles.find(article => article.id === parseInt(id));
}

// 最新のブログ記事を取得する関数（件数指定可能）
function getLatestBlogArticles(count = 3) {
    return blogArticles
        .sort((a, b) => new Date(b.date) - new Date(a.date))
        .slice(0, count);
}
